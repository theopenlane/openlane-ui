// Generates the FGA permission catalog types (AccessEnum + TAccessRole) from the backend.
//
// Source of truth: the `organization` type relations in theopenlane/core's assembled FGA
// model, emitted by `task fga:generate` as fga/model/generated/permissions.json. Those
// relations are exactly what GET /v1/account/roles/organization can return, so generating
// from them removes the hand-maintained mirror that previously drifted (e.g. the UI shipped
// `can_manage_groups` while the model defines `can_manage_group`).
//
// Source resolution order:
//   1. OPENLANE_PERMISSIONS_CATALOG env var -> read that local file (used for local dev /
//      against a sibling core checkout before the catalog is published).
//   2. otherwise fetch the published catalog from core's main branch (same mechanism the
//      GraphQL schema is pulled with in graphql-codegen.yml).

const fs = require('fs')
const path = require('path')

// Defaults to core's main branch; override OPENLANE_CORE_REF to pin a tag/SHA for reproducible builds.
const CORE_REF = process.env.OPENLANE_CORE_REF || 'main'
const CATALOG_URL = `https://raw.githubusercontent.com/theopenlane/core/${CORE_REF}/fga/model/generated/permissions.json`
const OUTPUT_PATH = path.join(__dirname, '..', 'src', 'permissions.generated.ts')

// A relation is surfaced as a named AccessEnum constant when it represents a permission
// (can_* relation) or the audit log viewer. Role/assignment tokens (owner, *_manager,
// *_creator, full_access, ...) are not checked by name in the UI, but they remain valid
// members of TAccessRole below since the roles endpoint can still return them.
const isAccessEnumRelation = (relation) => relation.startsWith('can_') || relation === 'audit_log_viewer'

// can_create_trust_center_doc -> CanCreateTrustCenterDoc
const toPascalCase = (relation) =>
  relation
    .split('_')
    .filter(Boolean)
    .map((part) => part.charAt(0).toUpperCase() + part.slice(1))
    .join('')

const loadCatalog = async () => {
  const localPath = process.env.OPENLANE_PERMISSIONS_CATALOG
  if (localPath) {
    return JSON.parse(fs.readFileSync(localPath, 'utf8'))
  }

  const res = await fetch(CATALOG_URL)
  // A 404 means the catalog isn't published on this ref yet — the only error the caller is allowed
  // to fall back on. Any other failure (network, 5xx, rate limit) is a real problem and must throw.
  if (res.status === 404) {
    const err = new Error(`permission catalog not published at ${CATALOG_URL} (404)`)
    err.notPublished = true
    throw err
  }
  if (!res.ok) {
    throw new Error(`failed to fetch permission catalog from ${CATALOG_URL}: ${res.status} ${res.statusText}`)
  }
  return res.json()
}

const buildOutput = (relations) => {
  const sorted = [...relations].sort()

  const enumMembers = sorted.filter(isAccessEnumRelation)

  // Guard against name collisions from the PascalCase mapping so a silent overwrite can't
  // mask two distinct relations sharing a generated key.
  const seen = new Map()
  for (const relation of enumMembers) {
    const key = toPascalCase(relation)
    if (seen.has(key) && seen.get(key) !== relation) {
      throw new Error(`AccessEnum name collision: "${key}" maps to both "${seen.get(key)}" and "${relation}"`)
    }
    seen.set(key, relation)
  }

  const lines = []
  lines.push('/* eslint-disable */')
  lines.push('// Code generated by plugins/generate-permissions.js; DO NOT EDIT.')
  lines.push('// Source: theopenlane/core fga/model/generated/permissions.json (organization relations).')
  lines.push('')
  lines.push('export enum AccessEnum {')
  for (const relation of enumMembers) {
    lines.push(`  ${toPascalCase(relation)} = '${relation}',`)
  }
  lines.push('}')
  lines.push('')
  lines.push('/** Every relation the organization roles endpoint can return for a subject. */')
  lines.push('export type TAccessRole =')
  lines.push(sorted.map((relation) => `  | '${relation}'`).join('\n'))
  lines.push('')

  return lines.join('\n')
}

;(async () => {
  let catalog
  try {
    catalog = await loadCatalog()
  } catch (err) {
    // Only a not-yet-published catalog (404) is recoverable: keep the committed file and warn.
    // Every other failure must propagate so codegen/CI fails loudly instead of shipping stale types.
    if (err.notPublished && fs.existsSync(OUTPUT_PATH)) {
      console.warn(`generate-permissions: ${err.message}; keeping existing ${path.basename(OUTPUT_PATH)}`)
      return
    }
    throw err
  }

  const relations = catalog && Array.isArray(catalog.organization) ? catalog.organization : null
  if (!relations || relations.length === 0) {
    throw new Error('permission catalog is missing a non-empty "organization" array')
  }

  fs.writeFileSync(OUTPUT_PATH, buildOutput(relations))
  console.log(`generated ${path.relative(path.join(__dirname, '..'), OUTPUT_PATH)} with ${relations.length} relation(s)`)
})().catch((err) => {
  console.error(err.message)
  process.exit(1)
})
