const fs = require('fs')
const path = require('path')

const introspectionPath = path.join(__dirname, '..', 'src', 'introspectionschema.json')
const outputPath = path.join(__dirname, '..', 'src', 'merge-fields.generated.ts')

const raw = JSON.parse(fs.readFileSync(introspectionPath, 'utf8'))
const schema = raw.__schema || raw

const unwrapType = (t) => {
  let kind = t.kind
  let name = t.name
  let ofType = t.ofType
  let list = false
  while (ofType) {
    if (kind === 'LIST') list = true
    kind = ofType.kind
    name = ofType.name
    ofType = ofType.ofType
  }
  return { leafKind: kind, leafName: name, list }
}

const isMergeableLeafKind = (kind) => kind === 'SCALAR' || kind === 'ENUM'

const implementsNode = (type) => Array.isArray(type.interfaces) && type.interfaces.some((i) => i.name === 'Node')

const typesByName = new Map(schema.types.map((t) => [t.name, t]))

// Build the set of writable field names for every Update*Input. The merge sheet
// only surfaces fields that exist on BOTH the readable Object type AND its
// corresponding update input — otherwise a user could pick a value during merge
// that the backend silently drops at mutation time.
const writableFieldsByInputName = new Map()
for (const type of schema.types) {
  if (type.kind !== 'INPUT_OBJECT') continue
  if (!Array.isArray(type.inputFields)) continue
  writableFieldsByInputName.set(type.name, new Set(type.inputFields.map((f) => f.name)))
}

const fieldsByType = {}
const typeNames = []

for (const type of schema.types) {
  if (type.kind !== 'OBJECT') continue
  if (!implementsNode(type)) continue
  if (!Array.isArray(type.fields)) continue

  const updateInputName = `Update${type.name}Input`
  const writableFields = writableFieldsByInputName.get(updateInputName)
  if (!writableFields) continue

  const descriptors = []
  for (const field of type.fields) {
    if (Array.isArray(field.args) && field.args.length > 0) continue
    if (!writableFields.has(field.name)) continue
    const { leafKind, leafName, list } = unwrapType(field.type)
    if (!isMergeableLeafKind(leafKind)) continue
    const descriptor = { name: field.name, list }
    if (leafKind === 'SCALAR') descriptor.scalarName = leafName
    if (leafKind === 'ENUM') descriptor.enumName = leafName
    descriptors.push(descriptor)
  }

  if (descriptors.length === 0) continue
  fieldsByType[type.name] = descriptors
  typeNames.push(type.name)
}

typeNames.sort()

const stringifyDescriptor = (d) => {
  const parts = [`name: ${JSON.stringify(d.name)}`, `list: ${d.list}`]
  if (d.scalarName) parts.push(`scalarName: ${JSON.stringify(d.scalarName)}`)
  if (d.enumName) parts.push(`enumName: ${JSON.stringify(d.enumName)}`)
  return `{ ${parts.join(', ')} }`
}

const lines = []
lines.push('/* eslint-disable */')
lines.push('// This file is auto-generated by plugins/generate-merge-fields.js. Do not edit manually.')
lines.push('// Source: introspectionschema.json (regenerated by `task codegen:codegen`).')
lines.push('//')
lines.push('// For each GraphQL type that implements Node AND has a corresponding Update*Input,')
lines.push('// emits every scalar/enum (or list-of) field that appears on both the readable Object')
lines.push('// type and its update input. That intersection is the authoritative set of fields a')
lines.push('// merge sheet can present — schema-derived end to end, no hand-maintained list.')
lines.push('')
lines.push('export type MergeFieldDescriptor = {')
lines.push('  readonly name: string')
lines.push('  readonly list: boolean')
lines.push('  readonly scalarName?: string')
lines.push('  readonly enumName?: string')
lines.push('}')
lines.push('')

if (typeNames.length === 0) {
  lines.push('export type MergeableTypeName = never')
} else {
  lines.push('export type MergeableTypeName =')
  typeNames.forEach((name, idx) => {
    const suffix = idx === typeNames.length - 1 ? '' : ''
    lines.push(`  | ${JSON.stringify(name)}${suffix}`)
  })
}
lines.push('')
lines.push('export const MERGEABLE_FIELDS_BY_TYPE: {')
lines.push('  readonly [K in MergeableTypeName]: readonly MergeFieldDescriptor[]')
lines.push('} = {')
for (const typeName of typeNames) {
  const descriptors = fieldsByType[typeName]
  lines.push(`  ${JSON.stringify(typeName)}: [`)
  for (const d of descriptors) {
    lines.push(`    ${stringifyDescriptor(d)},`)
  }
  lines.push('  ],')
}
lines.push('}')
lines.push('')

fs.writeFileSync(outputPath, lines.join('\n'))
console.log(`Generated merge-fields.generated.ts with ${typeNames.length} types`)
